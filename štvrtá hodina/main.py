farba2 = random.choice(("black", "red", "blue", "yellow", "green"))
from random import randrange
a = randrange(randrange(50,150))
b = randrange(randrange(50,150))
def square():
    t.penup()
    firstpos = randrange(-300,300)
    secondpos = randrange(-300,300)
    t.pencolor(farba1)
    t.setposition(firstpos,secondpos)
    t.pendown()

    for i in range(4):
        t.forward(a)
        t.right(90)
def triangle():
    t.penup()
    firstpos = randrange(-300,300)
    secondpos = randrange(-300,300)
    t.pencolor(farba2)
    t.setposition(firstpos,secondpos)
    t.pendown()
    for i in range(3):
        t.forward(b)
        t.right(120)
